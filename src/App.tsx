function Question({ tweet }) {
  return (
    <div>
      <Typography variant="h6" padding={"10px"}>
        Did {tweet.name} tweet this or was it generated by GPT?
      </Typography>
    </div>
  );
}

function TweetCard({ tweet }) {
  return (
    <Card>
      <Tweet tweet={tweet} />
    </Card>
  );
}

function AnswerOptions({
  tweet,
  setCurrentStreak,
  currentStreak,
  setTweet,
  setLoseGame,
  setloseGameByChoosingAIForHumanAnswer,
}) {
  const answerHandler = (givenAnswer: boolean) => {
    const correctAnswer = tweet.isReal === givenAnswer;

    if (correctAnswer) {
      setCurrentStreak(currentStreak + 1);
      setTweet(getRandomTweet(data));
    } else {
      if (givenAnswer === true) {
        setLoseGame(true);
      } else {
        setloseGameByChoosingAIForHumanAnswer(true);
      }
    }
  };

  return (
    <div style={{ padding: 20 }}>
      <Grid container spacing={10} justifyContent={"center"}>
        <Grid item>
          <Button
            variant="contained"
            color="primary"
            size="large"
            startIcon={<EditIcon />}
            style={{
              fontWeight: "bold",
              textTransform: "none",
              color: "white",
            }}
            onClick={() => answerHandler(true)}
          >
            {tweet.name}
          </Button>
        </Grid>
        <Grid item>
          <Button
            variant="contained"
            color="primary"
            size="large"
            endIcon={<AssistantIcon />}
            style={{
              fontWeight: "bold",
              textTransform: "none",
              color: "white",
            }}
            onClick={() => answerHandler(false)}
          >
            <Typography>GPT</Typography>
          </Button>
        </Grid>
      </Grid>
    </div>
  );
}

function CurrentStreak({ currentStreak }) {
  const label = "Correct guess streak: " + currentStreak;

  return (
    <div>
      <Chip label={label} color="primary"></Chip>
    </div>
  );
}

function Frame({
  setLoseGame,
  currentStreak,
  setCurrentStreak,
  tweet,
  setTweet,
  setloseGameByChoosingAIForHumanAnswer,
}) {
  return (
    <>
      <Question tweet={tweet}></Question>
      <TweetCard tweet={tweet}></TweetCard>
      <AnswerOptions
        tweet={tweet}
        setCurrentStreak={setCurrentStreak}
        currentStreak={currentStreak}
        setTweet={setTweet}
        setLoseGame={setLoseGame}
        setloseGameByChoosingAIForHumanAnswer={
          setloseGameByChoosingAIForHumanAnswer
        }
      ></AnswerOptions>
      <div style={{ padding: "10px" }}>
        <CurrentStreak currentStreak={currentStreak}></CurrentStreak>
      </div>
    </>
  );
}

